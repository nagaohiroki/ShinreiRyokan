class arcFile
{
public:
	unsigned long size;
	unsigned long point;
	TCHAR name[MAX_PATH];
	arcFile()
	{
		size = 0;
		point = 0;
		_stprintf_s(name,MAX_PATH,_T(""));
	}
	VOID Set(TCHAR* Name,unsigned long Size,unsigned long Point)
	{
		size = Size;
		point = Point;
		_stprintf_s(name,MAX_PATH,_T("%s"),Name);
	}
};
